using System;
namespace dotNetAcademy
{
    public class Csharp22
    {  public static void Main(string [] args)
        {
            //increment and decrement values
            int value = 0;
            Console.WriteLine(value);
            // variable add value
            value = value + 5;
            Console.WriteLine(value);
            //add and assign
            value += 5;
            Console.WriteLine(value);
            //plusplus operator
            value ++;
            Console.WriteLine(value);
        }
    }
}